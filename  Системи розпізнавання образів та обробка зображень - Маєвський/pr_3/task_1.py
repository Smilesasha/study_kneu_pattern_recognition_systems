import numpy as np
import matplotlib.pyplot as plt

# -------------------------------------------------
# 1. Щільності розподілу
# -------------------------------------------------
def p1(x):
    return (1 / np.sqrt(np.pi)) * np.exp(-x**2)

def p2(x):
    return (1 / np.sqrt(np.pi)) * np.exp(-(x - 1)**2)

# -------------------------------------------------
# 2. Апріорні ймовірності
# -------------------------------------------------
P_w1 = 0.5
P_w2 = 0.5

# -------------------------------------------------
# 3. Матриця ризику
# L = [[lambda11, lambda12],
#      [lambda21, lambda22]]
# -------------------------------------------------
lambda11 = 0
lambda12 = 0.5
lambda21 = 1
lambda22 = 0

# -------------------------------------------------
# 4. Поріг мінімальної ймовірності помилки
# p(x|w1)P(w1) = p(x|w2)P(w2)
# exp(-x^2) = exp(-(x-1)^2)
# x = 1/2
# -------------------------------------------------
x_min_error = 0.5

# -------------------------------------------------
# 5. Поріг мінімального ризику
# p(x|w1) / p(x|w2) =
# ((lambda21 - lambda22) / (lambda12 - lambda11)) * (P(w2)/P(w1))
#
# Для даних задачі:
# p(x|w1)/p(x|w2) = (1 - 0)/(0.5 - 0) * (0.5/0.5) = 2
#
# exp(-x^2) / exp(-(x-1)^2) = 2
# -x^2 = ln(2) - (x-1)^2
# x = (1 - ln(2)) / 2
# -------------------------------------------------
x_min_risk = (1 - np.log(2)) / 2

# -------------------------------------------------
# 6. Виведення результатів
# -------------------------------------------------
print("Поріг для мінімальної ймовірності помилки:")
print(f"x* = {x_min_error:.6f}")

print("\nПоріг для мінімального ризику:")
print(f"x* = {x_min_risk:.6f}")

# -------------------------------------------------
# 7. Побудова графіків щільностей
# -------------------------------------------------
x = np.linspace(-2, 3, 1000)
y1 = p1(x)
y2 = p2(x)

plt.figure(figsize=(10, 6))
plt.plot(x, y1, label=r'$p(x|\Omega_1)=\frac{1}{\sqrt{\pi}}e^{-x^2}$', linewidth=2)
plt.plot(x, y2, label=r'$p(x|\Omega_2)=\frac{1}{\sqrt{\pi}}e^{-(x-1)^2}$', linewidth=2)

# Вертикальні лінії порогів
plt.axvline(x_min_error, color='red', linestyle='--', label=f'Поріг мін. помилки = {x_min_error:.4f}')
plt.axvline(x_min_risk, color='green', linestyle='--', label=f'Поріг мін. ризику = {x_min_risk:.4f}')

plt.title('Графіки щільностей розподілу')
plt.xlabel('x')
plt.ylabel('p(x)')
plt.grid(True)
plt.legend()
plt.show()